def getProxies():
    proxies = []
    for page in range(1,11):
        url = "https://www.kuaidaili.com/free/inha/"+str(page)
        res = requests.get(url)
        html = etree.HTML(res.text)
        
        ips = html.xpath('//tbody/tr/td[1]/text()')
        ports = html.xpath('//tbody/tr/td[2]/text()')
        #print(ips,ports)
        
        for (ip,port) in zip(ips,ports):
            proxy = 'http://'+ip+":"+port
            #print(proxy)
            proxies.append(proxy) 
        time.sleep(2)
    return proxies 


def testProxies(init_proxies):
    
    pool = Pool(processes=4)
    proxies = pool.map(testProxy,init_proxies)
    pool.close()
    pool.join()
    
    ip_list = []
    for proxy in proxies:
        if(proxy):
            ip_list.append(proxy)
    return ip_list

def testProxy(proxy):
    url = "http://www.jianshu.com/p/761e7b6c0a85"
    try:
        req = requests.get(url,headers = {'User-Agent':getUsrAgent()},proxies = {'proxy':proxy},timeout = 3.0)
        if(req.status_code==200):
            return proxy
        else:return None
    except Exception as e:
        print("验证代理IP（"+proxy+"）时发生如下错误）：")
        print(e)
    return ""
   
